			( -- OTROS --
			SELECT PAIS.OID_PAIS,
				   SUBA.OID_SBAC,
				   PAIS.MONE_OID_MONE,
				   PAIS.MONE_OID_MONE_ALT,
				   TIP_MED_PAG.OID_TIPO_MEDI_PAGO,
