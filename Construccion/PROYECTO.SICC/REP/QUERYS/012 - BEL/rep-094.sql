				  AND SOL_CAB.PAIS_OID_PAIS = PAIS.OID_PAIS
				  AND SOL_CAB.SBAC_OID_SBAC = SUBA.OID_SBAC
				  AND MOV_CAJA_CAB.SOCA_OID_SOLI_CABE = SOL_CAB.OID_SOLI_CABE
				  AND SOL_CAB.VAL_NUME_SOLI IS NOT NULL
				  AND MOV_CAJA_DET.MCAC_OID_CABE = MOV_CAJA_CAB.OID_CABE
				  AND MOV_CAJA_DET.MPBC_OID_MEDI_PAGO_BELC = MED_PAG_BEL.OID_MEDI_PAGO_BELC
				  AND MED_PAG_BEL.MPAB_OID_MEDI_PAGO = MED_PAG.OID_MEDI_PAGO
				  AND TIP_MED_PAG.OID_TIPO_MEDI_PAGO = MED_PAG.TMEP_OID_TIPO_MEDI_PAGO
				  AND MED_PAG.PAIS_OID_PAIS = PAIS.OID_PAIS
			      AND TIP_MED_PAG.OID_TIPO_MEDI_PAGO = 3
			--	  AND PAIS.OID_PAIS = 1  										-- PARAMETRO DE PAIS --
			--	  AND OID_CANA = 1	  											-- PARAMETRO DE CANAL --
			--	  AND ACCE.OID_ACCE = 1											-- PARAMETRO DE ACCESO --
			--	  AND SUBA.OID_SBAC = 1											-- PARAMETRO DE SUBACCESO --
			--	  AND TO_DATE(MOV_CAJA_CAB.FEC_MOVI) = TO_DATE('01/03/2005', 'DD/MM/YYYY')     -- PARAMETRO FECHA DE CIERRE --	
			)
		UNION ALL
			( -- VALES DE COMPRA --
			SELECT PAIS.OID_PAIS,
				   SUBA.OID_SBAC,
				   PAIS.MONE_OID_MONE,
				   PAIS.MONE_OID_MONE_ALT,
				   TIP_MED_PAG.OID_TIPO_MEDI_PAGO,
				   MED_PAG.OID_MEDI_PAGO,
			  	   (
				   SELECT MOV_CAJA_DET.IMP_MOVI
				   FROM SEG_MONED MONE	   		 
				   WHERE MOV_CAJA_DET.MONE_OID_MONE = MONE.OID_MONE
						 AND MONE.OID_MONE = PAIS.MONE_OID_MONE
				   ) MONE,
			   	   (
				   SELECT MOV_CAJA_DET.IMP_DIVI
				   FROM	 SEG_MONED MONE	   		 
				   WHERE MOV_CAJA_DET.MONE_OID_MONE = MONE.OID_MONE
