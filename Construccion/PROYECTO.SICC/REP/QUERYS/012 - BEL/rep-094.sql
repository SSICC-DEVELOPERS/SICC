			FROM SEG_PAIS PAIS,
				 SEG_CANAL CANAL,
				 SEG_ACCES ACCE,
				 SEG_SUBAC SUBA,
				 PED_SOLIC_CABEC SOL_CAB,
				 BEL_MOVIM_CAJA_CABEC MOV_CAJA_CAB,
				 BEL_MOVIM_CAJA_DETAL MOV_CAJA_DET,
				 BEL_MEDIO_PAGO_BELCE MED_PAG_BEL,
				 BEL_MEDIO_PAGO MED_PAG,
				 BEL_TIPO_MEDIO_PAGO TIP_MED_PAG		 	
			WHERE CANAL.OID_CANA = ACCE.CANA_OID_CANA
				  AND ACCE.OID_ACCE = SUBA.ACCE_OID_ACCE
				  AND SOL_CAB.PAIS_OID_PAIS = PAIS.OID_PAIS
				  AND SOL_CAB.SBAC_OID_SBAC = SUBA.OID_SBAC
				  AND MOV_CAJA_CAB.SOCA_OID_SOLI_CABE = SOL_CAB.OID_SOLI_CABE
				  AND SOL_CAB.VAL_NUME_SOLI IS NOT NULL
				  AND MOV_CAJA_DET.MCAC_OID_CABE = MOV_CAJA_CAB.OID_CABE
